int		ft_count_if(char **tab, int (*f)(char*))
{
	int	i;
	int num;

	i = 0;
	num = 0;
	while (tab[i])
	{
		if (f(tab[i]) == 1)
		{
			num = num + 1;
		}
		i++;
	}
	return (num);
}

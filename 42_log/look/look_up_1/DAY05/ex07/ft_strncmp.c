int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	if (!n)
	{
		return (0);
	}
	while (*s1 == *s2 && --n && *s1)
	{
		if (*s1 == '\0')
		{
			return (0);
		}
		s1 += 1;
		s2 += 1;
	}
	return (*s1 - *s2);
}

char	*ft_strcat(char *dest, char *src)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
		i += 1;
	while (src[j] != '\0')
	{
		dest[i + j] = src[j];
		j += 1;
	}
	return (dest);
}

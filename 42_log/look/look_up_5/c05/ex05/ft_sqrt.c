int	ft_sqrt(int nb)
{
	int	x;
	int	y;

	if (nb < 1)
		return (0);
	x = nb;
	y = 1;
	while (x > y)
	{
		x = (x + y) / 2;
		y = nb / x;
	}
	if (x * x != nb)
		return (0);
	return (x);
}

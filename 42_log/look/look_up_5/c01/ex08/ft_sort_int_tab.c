void	ft_swap(int *a, int *b)
{
	int	temp;

	temp = *a;
	*a = *b;
	*b = temp;
}

void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	min_idx;

	i = 0;
	while (i < size - 1)
	{
		min_idx = i;
		j = i + 1;
		while (j < size)
		{
			if (*(tab + j) < *(tab + min_idx))
				min_idx = j;
			j++;
		}
		ft_swap(tab + i, tab + min_idx);
		i++;
	}
}
